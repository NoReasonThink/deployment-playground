import type { NodeKind, ScenarioDefinition, ScenarioId } from '../types'
import { buildNodeData } from './catalog'

function buildScenarioDsl(options: {
  qpsMin: number
  p95Max: number
  errorRateMax: number
  availabilityMin: number
  costMax: number
  minScore: number
  minStars: number
  maxNodes: number
  maxEdges: number
  maxBudget: number
  requiredKinds: NodeKind[]
  prerequisiteScenarioIds: ScenarioId[]
}): Pick<ScenarioDefinition, 'goals' | 'constraints' | 'scoreWeights' | 'passCriteria' | 'unlockRule'> {
  return {
    goals: [
      { id: 'qps-target', label: `QPS ≥ ${options.qpsMin}`, metric: 'qps', operator: 'gte', target: options.qpsMin, weight: 0.2 } as const,
      { id: 'latency-target', label: `P95 ≤ ${options.p95Max}ms`, metric: 'p95LatencyMs', operator: 'lte', target: options.p95Max, weight: 0.24 } as const,
      { id: 'error-target', label: `错误率 ≤ ${options.errorRateMax}%`, metric: 'errorRate', operator: 'lte', target: options.errorRateMax, weight: 0.18 } as const,
      { id: 'availability-target', label: `可用性 ≥ ${options.availabilityMin}%`, metric: 'availability', operator: 'gte', target: options.availabilityMin, weight: 0.2 } as const,
      { id: 'cost-target', label: `成本 ≤ ${options.costMax}`, metric: 'estimatedCost', operator: 'lte', target: options.costMax, weight: 0.18 } as const,
    ],
    constraints: {
      maxNodes: options.maxNodes,
      maxEdges: options.maxEdges,
      maxBudget: options.maxBudget,
      requiredKinds: options.requiredKinds,
    },
    scoreWeights: {
      performance: 0.3,
      stability: 0.27,
      cost: 0.16,
      recoverability: 0.15,
      security: 0.12,
    },
    passCriteria: {
      minScore: options.minScore,
      minStars: options.minStars,
      maxErrorRate: options.errorRateMax,
      minAvailability: options.availabilityMin,
    },
    unlockRule: {
      prerequisiteScenarioIds: options.prerequisiteScenarioIds,
      minStars: options.minStars,
    },
  }
}

